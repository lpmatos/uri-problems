#!/usr/bin/ruby

# =============================================================================
# GLOBAL VARIABLE
# =============================================================================

$codes = (%w(1 2 3 4 5)).map(&:to_i)
$prices = (%w(4 4.50 5 2 1.5)).map(&:to_f)

# =============================================================================
# MAIN
# =============================================================================

if __FILE__ == $0
    valores = ((STDIN.gets).strip).split(" ")
    puts "#{valores.class}"
    puts "#{valores}"
    puts "#{$codes}"
    puts "#{$prices}"
end
