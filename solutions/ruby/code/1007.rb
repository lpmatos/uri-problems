#!/usr/bin/ruby

# =============================================================================
# FUNCTIONS
# =============================================================================

def calculo(*args)
    if args.size == 4
        puts "DIFERENCA = #{(args[0] * args[1]) - (args[2] * args[3])}\n"
    end
end

# =============================================================================
# MAIN
# =============================================================================

if __FILE__ == $0
    A, B, C, D = gets.strip.to_i, gets.strip.to_i, gets.strip.to_i, gets.strip.to_i
    calculo(A, B, C, D)
end
