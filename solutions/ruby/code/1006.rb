#!/usr/bin/ruby

# =============================================================================
# FUNCTIONS
# =============================================================================

def media(A, B, C)
    return ((A * 2) + (B * 3) + (C * 5))/10
end

# =============================================================================
# MAIN
# =============================================================================

if __FILE__ == $0
    A, B, C = gets.strip.to_f
    puts sprintf "MEDIA = %.1f\n" % (media(A, B, C))
end
