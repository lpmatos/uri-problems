#!/usr/bin/ruby

# =============================================================================
# FUNCTIONS
# =============================================================================

def media(a, b, c)
    return ((a * 2) + (b * 3) + (c * 5))/10
end

# =============================================================================
# MAIN
# =============================================================================

if __FILE__ == $0
    A, B, C = gets.strip.to_f, gets.strip.to_f, gets.strip.to_f
    puts sprintf "MEDIA = %.1f\n" % (media(A, B, C))
end
